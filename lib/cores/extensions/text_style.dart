extension TextStyleExtension on TextStyle {
  //Font Weight
  TextStyle get thin => copyWith(fontWeight: FontWeight.w100);
  TextStyle get extraLight => copyWith(fontWeight: FontWeight.w200);
  TextStyle get light => copyWith(fontWeight: FontWeight.w300);
  TextStyle get normal => copyWith(fontWeight: FontWeight.w400);
  TextStyle get medium => copyWith(fontWeight: FontWeight.w500);
  TextStyle get semiBold => copyWith(fontWeight: FontWeight.w600);
  TextStyle get bold => copyWith(fontWeight: FontWeight.bold);
  TextStyle get extraBold => copyWith(fontWeight: FontWeight.w800);
  TextStyle get thick => copyWith(fontWeight: FontWeight.w900);

  // Font Style & Text Decoration
  TextStyle get italic => copyWith(fontStyle: FontStyle.italic);
  TextStyle get overline => copyWith(decoration: TextDecoration.overline);
  TextStyle get underline => copyWith(decoration: TextDecoration.underline);
  TextStyle get lineThrough => copyWith(decoration: TextDecoration.lineThrough);

  // Font Color
  TextStyle get primaryColor => copyWith(color: AppColors.lightPrimaryColor);
  TextStyle get red => copyWith(color: AppColors.appRed);
  TextStyle get lightyellow => copyWith(color: AppColors.lightyellow);
  TextStyle get yellow => copyWith(color: AppColors.yellow);
  TextStyle get white => copyWith(color: Colors.white);
  TextStyle get black => copyWith(color: AppColors.black);
  TextStyle get brown => copyWith(color: AppColors.brown);
  TextStyle get lightGrey => copyWith(color: AppColors.lightGrey);
  TextStyle get grey => copyWith(color: AppColors.grey);
  TextStyle get darkGrey => copyWith(color: AppColors.darkGrey);
}

